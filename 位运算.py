# 位与运算符 &
# 只有两个数都为 1 才为 1 否则为0

# 位或运算符|
# 都是 0 结果才是 0 否则为 1

# 位异或运算符^
# 同时为 0 或 同时为 1 结果为 0 否则为 1

# 取反运算符~
# 将 1 变成 0 把 0 变成 1

# 左移位运算 <<

# 右移位运算 >>

print("12 & 8 = " + str(12 & 8))
print("12 | 8 = " + str(12 | 8))
print("12 ^ 8 = " + str(12 ^ 8))
print("~12 = " + str(~12))  # 涉及补码  将整个字长全部取反
print("~8 = " + str(~8))
print("2 << 1 = " + str(2 << 1))
print("2 >> 2 = " + str(2 >> 1))
